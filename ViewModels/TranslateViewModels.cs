    public class TranslateViewModels : BaseViewModel
    {
        //<string, string, string>; <język, nazwa_pola, translate>
        // Pole przechowujące domyślny język
        private string defaultLanguage;
        private TranslationManager translationManager;

        public TranslateViewModels()
        {
            defaultLanguage = Preferences.Default.Get("CurrentLanguage", "polski");
            translationManager = new TranslationManager();  
        }

        public string TranslatedText
        {
            get
            {
                // Tutaj wywołaj odpowiednią metodę z TranslationManager do tłumaczenia tekstu
                return translationManager.Translate(defaultLanguage, "ContinueLabel"); // Przykładowy tekst do tłumaczenia
            }
        }

        public void SetLanguage(string language)
        {
            // Ustawienie nowego języka w preferencjach
            Preferences.Set("ChosenLanguage", language);

            // Aktualizacja domyślnego języka
            defaultLanguage = language;

            // Tutaj możesz dodać dodatkową logikę, na przykład odświeżenie interfejsu użytkownika,
            // aby odzwierciedlić zmiany języka
        }
    }

