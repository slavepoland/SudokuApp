        // Metoda do dodawania tłumaczeń dla określonego języka
        public void AddTranslations(string language, Dictionary<string, string> translationsForLanguage)
        {
            if (!translations.TryAdd(language, translationsForLanguage))
            {
                // Jeśli tłumaczenia dla tego języka już istnieją, zastąp je nowymi
                translations[language] = translationsForLanguage;
            }
        }

        // Metoda do tłumaczenia tekstu na aktualnie wybrany język
        public string Translate(string language, string fieldName)
        {
            // Sprawdź, czy tłumaczenia dla danego języka istnieją
            if (translations.TryGetValue(language, out Dictionary<string, string> value))
            {
                var translationDictionary = value;

                // Sprawdź, czy pole o podanej nazwie istnieje w słowniku tłumaczeń
                if (translationDictionary.TryGetValue(fieldName, out string value1))
                {
                    // Zwróć tekst odpowiadający nazwie pola
                    return value1;
                }
            }
            // Jeśli tłumaczenie nie istnieje, zwróć oryginalny tekst
            return "";
        }

        // Metoda do ustawiania języka
        public void SetLanguage(string language)
        {
            // Ustaw nowy język w preferencjach
            Preferences.Set("CurrentLanguage", language);

            // Zaktualizuj domyślny język
            DefaultLanguage = language;
        }

        // Metoda do pobierania aktualnego języka
        public string GetLanguage()
        {
            return DefaultLanguage;
        }
    }
